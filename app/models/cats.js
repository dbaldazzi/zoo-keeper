class Cat {
  constructor(
    type = '',
    name = '',
    gender = '',
    weight = 0,
    tailLength = 0,
    teethSize = '',
  ) {
    this.type = type;
    this.name = name;
    this.gender = gender;
    this.weight = weight;
    this.tailLength = tailLength;
    this.teethSize = teethSize;
  }
}

export default Cat; 